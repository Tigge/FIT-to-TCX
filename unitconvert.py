"""Unit conversions"""

from pytz import UTC

def semicircle_to_degrees(semicircles):
    """Convert a number in semicricles to degrees"""
    return semicircles * (180.0 / 2.0 ** 31)


def local_date_to_utc(date):
    """Local date to UTC"""
    return date.replace(tzinfo=UTC)

